    private void printrecord(JPanel printPanel){
       
        PrinterJob pj =  PrinterJob.getPrinterJob();
        pj.setJobName("Print Salary");
        pj.setPrintable(new Printable() {
            @Override
            public int print(Graphics graphics, PageFormat pageFormat, int pageIndex) throws PrinterException {
                if(pageIndex>0){
                    return Printable.NO_SUCH_PAGE;
                }            
                Graphics2D gr2d = (Graphics2D)graphics;
                gr2d.translate(pageFormat.getImageableX()*2, pageFormat.getImageableY()*2);
                gr2d.scale(0.5, 0.5);
                printPanel.paint(gr2d);
                return Printable.PAGE_EXISTS;
                
                
            }
        });
         boolean returningresult =  pj.printDialog();
         if(returningresult){
             try {
                 pj.print();
             } catch (PrinterException e) {
                 JOptionPane.showMessageDialog(this, "Print error : " + e.getMessage());
             }
         }
    }
